    <p class="mt-4">
      În momentul de față ansamblul a fost preluat de către Fundația Demnitatea
      Pontică din Constanța. În anul 2012 ansamblul a susținut un spectacolul
      aniversar, spectacol care a reprezentat în același timp și spectacolul de
      revenire a ansamblului pe scena dansului popular.
    </p>
