    <p class="mt-4">
      Când vorbim despre o formaţie ori ansamblu de dans popular aici în
      Dobrogea, numele care ne vine pe buze aproape automat este „Brâuleţul”.
      Acest nume a intrat aşa de mult în mentalul colectiv încât ni se pare
      firesc să auzim de el că evoluează pe o scenă sau pe alta. Şi dacă la
      vremea la care aceste ansambluri populare s-au înfiinţat (respectiv anii
      ’50) „Brâuleţul” era o marcă neînregistrată, spre deosebire de alte
      ansambluri care erau mărci înregistrate precum „Ciprian Porumbescu” pentru
      Suceava ori „Periniţa” pentru Bucureşti, la ora actuală ansamblul este
      marcă înregistrată.
    </p>
    <p>
      Când s-a constituit ansamblul trupa de dansatori amatori s-a reunit sub
      bagheta maestrului coregraf Ion Buruc, prin anul 1953. Aceasta a
      constituit în fapt prima formă organizată de dansuri populare din
      Dobrogea. S-a numit „Brâuleţul”. A funcţionat ca formaţie de amatori până
      în 15 octombrie 1962.
    </p>
